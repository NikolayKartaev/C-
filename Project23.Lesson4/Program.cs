void PrintImage(int[,] image)
{
    for (int rows = 0; rows < image.GetLength(0); rows++)
    {
        for (int columns = 0; columns < image.GetLength(1); columns++)
        {
            if (image[rows, columns] == 0) Console.Write($" ");
            else Console.Write("*");
        }
        Console.WriteLine();
    }
}
void FillImage(int row, int column)
{
    if (picture[row, column] == 0)
    {
        picture[row, column] = 1;
        FillImage(row - 1, column); // Рекурентное соотношение (Рекурсия) - функция вызывающая саму себя
        FillImage(row, column - 1);
        FillImage(row + 1, column);
        FillImage(row, column + 1);
    }
}

